//

#include <iostream>
#include <SFML/Graphics.hpp>
#include <SFML/Window.hpp>
#include <SFML/System.hpp>
#include <SFML/Audio.hpp>
using namespace sf;
using namespace std;
int main()
{
    RenderWindow window(VideoMode(500, 500), "space ranger", Style::Default);
    Texture texture;
    if (!texture.loadFromFile("assets/spacecraft.png")) {
        cout << "Cannot load\n";
        return 0;
    } 
    Sprite enenmySprite;
    enenmySprite.setTexture(texture);
    while (window.isOpen()) {
        Event event;
        while (window.pollEvent(event)) {
            if (event.type == Event::Closed) {
                window.close();
            }
        }
        window.clear();
        window.draw(enenmySprite);
        window.display();
    }
    
    return 0;
}

